package Conditional_Statements;

public class Conditional_Statement {
	
	/*If Else Statement*/
	public static void main (String [] args){
		
		int Val1 = 10;
		int Val2 = 20;
		
		if (Val1 == Val2){
			
			System.out.println("Equal Value");
			
		}else {
			
			System.out.println("Unequal Value");
			
		}
	}

}
